def somma(a, b) -> int:
    c = a + b
    return c

def differenza(a, b) -> int:
    c = a - b
    return c

def moltiplicazione(a, b) -> int:
    c = a * b
    return c

def divisione(a,b) -> int:
    c = a / b
    return c